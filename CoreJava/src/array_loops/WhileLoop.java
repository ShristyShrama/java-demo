package array_loops;

public class WhileLoop {

	public static void main(String[] args) {
		
		int i = 1;
		
		while(i<=6)
		{
			System.out.println("value of i: "+i);
			i++;
		}
		
		System.out.println();
		
		int j = 6;
		
		while(j>=1)
		{
			System.out.println("value of j: "+j);
			j--;
		}

	}

}
